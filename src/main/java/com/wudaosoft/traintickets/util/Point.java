package com.wudaosoft.traintickets.util;

import java.io.Serializable;

/**
 * @author changsoul.wu
 *
 */
public class Point implements Serializable {

	private static final long serialVersionUID = -919172964411013162L;

	private int x;

	private int y;

	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "Point [x=" + x + ", y=" + y + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + x;
		result = prime * result + y;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Point other = (Point) obj;
		
		if (Math.abs(x - other.x) > 10)
			return false;
		if (Math.abs(y - other.y) > 10)
			return false;
		
		return true;
	}

	
}
